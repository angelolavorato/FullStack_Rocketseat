//Undefined e null são dois tipos de dados primitivos em JavaScript que representam a ausência de valor ou a falta de definição.
//Undefined é o valor atribuído a uma variável que foi declarada, mas não inicializada. Por exemplo:
let vazio
console.log('O Valor é:', vazio) // Saída: undefined

//Null é um valor atribuído a uma variável para indicar que ela não tem valor ou que o valor é desconhecido. Por exemplo:
let nulo = null
console.log('nulo é:', nulo) // Saída: null
