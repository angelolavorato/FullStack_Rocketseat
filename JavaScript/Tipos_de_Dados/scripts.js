//O JavaScript é uma linguagem dinâmica, ou seja, não é necessário declarar o tipo de dado de uma variável. O tipo de dado é determinado automaticamente pelo valor atribuído à variável. Além disso é possível alterar o tipo de dado de uma variável durante a execução do programa.
//Exemplo de declaração de variáveis com diferentes tipos de dados
let nome = 'João' // String
let idade = 30 // Number
let altura = 1.75 // Number
let isStudent = true // Boolean
let hobbies = ['futebol', 'música', 'viagem'] // Array
let endereco = { rua: 'Rua A', numero: 123, cidade: 'São Paulo' } // Object

//Tipos de dados primitivos
let nome2 = 'Maria' // String
let idade2 = 25 // Number
