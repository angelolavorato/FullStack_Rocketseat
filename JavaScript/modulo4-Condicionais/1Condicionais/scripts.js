//Operador condicional ternário
//ternário vem de 3 etapas
let age = 16
console.log(
  age >= 18 //condição
    ? 'Pode tirar carteira de motorista' //se for verdadeira
    : 'Não pode tirar carteira de motorista' //se for falsa
)

console.log(
  age < 18
    ? 'Pode tirar carteira de motorista'
    : 'Não pode tirar carteira de motorista'
)
