//IF ELSE - Estrutura de controle de fluxo que permite executar um bloco de código se uma condição for verdadeira e outro bloco de código se a condição for falsa.
//Exemplo 1 - Verificar se alguém é maior de idade
let age = 23
if (age >= 18) {
  console.log('Você é maior de idade!')
} else {
  //else é opcional, mas é usado para executar um bloco de código quando a condição do if for falsa e não precisa ter uma condicional dentro de parenteses, apenas o bloco de código a ser executado.
  console.log('Você é menor de idade!')
}
