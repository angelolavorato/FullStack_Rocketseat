//exemplo de incremento e decremento
let number = 10
console.log(number) // 10
//number = number + 1
number++ // Incremento
console.log(number) // 11

//mas e se eu quiser incrementar mais de 1?
console.log(number++) // 11
console.log(number) // 12

//Incrementar antes
console.log(++number) // 13

//Decremento
console.log('Decremento após: ', number--) // 13
console.log(number--) // 12
console.log('Decremento antes: ', --number) // 10

// Incrementar um número por outro valor
number += 5 // Incrementa 5
console.log(number) // 15

//Decrementar um número por outro valor
number -= 3 // Decrementa 3
console.log(number) // 12
