let balance = 500 //saldo da conta
let payment = 120 //valor do pagamento

//quero saber se o pagamento é maior ou menor que o saldo da conta
//> operador de comparação "maior que"
console.log(balance > payment) //true, o saldo da conta é maior que o valor do pagamento

//< operador de comparação "menor que"
console.log(balance < payment) //false, o saldo da conta é menor que o valor do pagamento

//>= operador de comparação "maior ou igual a"
balance = 120
console.log(balance >= payment) //true, o saldo da conta é maior ou igual ao valor do pagamento

//<= operador de comparação "menor ou igual a"
balance = 100
console.log(balance <= payment) //true, o saldo da conta é menor ou igual ao valor do pagamento
