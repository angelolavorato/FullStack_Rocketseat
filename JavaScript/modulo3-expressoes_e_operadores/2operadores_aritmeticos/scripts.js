//Operadores aritméticos
// + Adição / Concatenação
// - Subtração
// * Multiplicação
// / Divisão
// % Módulo (resto da divisão)
// ** Exponenciação
// Incremento (++)
// Decremento (--)

//Exemplos
console.log('Soma: ', 2 + 2) //Isso é uma soma
console.log('Concatenação: ', '2' + '2') //Isso é uma concatenação, pois estamos tratando os números como string
console.log('Subtração: ', 10 - 5) //Isso é uma subtração
console.log('Multiplicação: ', 10 * 5) //Isso é uma multiplicação
console.log('Divisão: ', 10 / 5) //Isso é uma divisão
console.log('Módulo: ', 10 % 3) //Isso é um módulo, o resultado é o resto da divisão
console.log('Exponenciação: ', 2 ** 3) //Isso é uma exponenciação, o resultado é 2 elevado a 3
