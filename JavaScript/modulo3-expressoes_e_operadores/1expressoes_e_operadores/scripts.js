//Operadores são símbolos que realizam operações em operandos (valores ou variáveis).
//São utilizados para manipular e comparar valores, realizar operações aritméticas, lógicas, de atribuições, entre outras.
//Expressão aritmética que soma dois números:
let sum = 5 + 3 + 7 // A expressão é "5 + 3 + 7" e o resultado é 15
console.log(sum) // Imprime 15

let average = sum / 3 // A expressão é "sum / 3" e o resultado é 5
console.log(average) // Imprime 5

//Expressão de comparação que verifica se um número é maior que outro:
let isGreater = 10 > 5 // A expressão é "10 > 5" e o resultado é true
console.log(isGreater) // Imprime true

//Expressão lógica que combina duas condições:
let isAdult = true
let hasID = false
let canEnter = isAdult && hasID // A expressão é "isAdult && hasID" e o resultado é false
console.log(canEnter) // Imprime false
