//Number é um tipo de variável para se trabalhar com números, seja ele inteiro ou decimal. Ele é representado por um número literal, ou seja, um número escrito diretamente no código.

console.log(typeof 1234) //Inteiro Positivo
console.log(typeof -1234) //Inteiro Negativo
console.log(typeof 3.14) //Decimal Positivo, não utilizar , e sim . para separar as casas decimais
console.log(typeof -3.14) //Decimal Negativo

console.log(12.5 / 'alo') //NaN - Not a Number, ou seja, não é um número, pois não é possível realizar a operação de divisão entre um número e uma string
console.log(56 / 8) //7 - resultado da divisão entre dois números
